namespace HEUSS.Core.Enums;

/// <summary>
/// Regional development level affecting infrastructure availability
/// </summary>
public enum DevelopmentLevel
{
    /// <summary>
    /// Subsistence living: minimal infrastructure, primarily biological energy
    /// </summary>
    Subsistence,

    /// <summary>
    /// Developing: basic infrastructure emerging, limited electricity/water
    /// </summary>
    Developing,

    /// <summary>
    /// Emerging: moderate infrastructure, improving access to electricity/water
    /// </summary>
    Emerging,

    /// <summary>
    /// Developed: full infrastructure, universal electricity/water/road access
    /// </summary>
    Developed
}
